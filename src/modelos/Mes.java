package modelos;

public class Mes {

	private String nome_do_mes;
	private String numero_do_mes;
	private int valor = 0;
	
	public String getNome_do_mes() {
		return nome_do_mes;
	}
	public void setNome_do_mes(String nome_do_mes) {
		this.nome_do_mes = nome_do_mes;
	}
	public String getNumero_do_mes() {
		return numero_do_mes;
	}
	public void setNumero_do_mes(String numero_do_mes) {
		this.numero_do_mes = numero_do_mes;
	}
	public int getValor() {
		return valor;
	}
	public void setValor(int valor) {
		this.valor = valor;
	}
	
}
